//多位数字显示
void print_num(int num1,int height,int width,int x,int y,int color)
{
	int num_length = 0,num_save;

	num_save=num1;
	//得到该数字是几位数
	while(num_save) 
	{
		num_length++;
		num_save = num_save/10;
	}
	if(num1 == 0)
	{
		num_length = 1;
	}
	num_save = num1;
	//是几位数就创建多大的数组
	int a[num_length];
	//从数组的最高位存放该数据的最低位
	int i;
	for(i = num_length - 1 ;i >= 0 ;i-- ) 
	{
		a[i] = num_save % 10;
		num_save = num_save / 10;
	}
	//先数组的最低位即数据的最高位
	for(i = 0 ;i < num_length ;i++ ) 
	{
		//每个解析出来的数字都分别打印 以及初始打印的位置
		print_word(num[a[i]],height,width,x,y,color);
		x = x + 26;
	}
	return;
}
//图片显示
void draw_pic(char *pic_ops,int x,int y)
{
	int fd_pic = open(pic_ops,O_RDONLY);
	if(-1 == fd_pic)
	{
		printf("Open error !\n");
		return ;
	}
	unsigned char buf[SIZE_OF_PIC];
	//lseek(fd_pic,54,SEEK_SET);
	read(fd_pic,buf,SIZE_OF_PIC);

	//宽度存储在0x12后面的四个字节中
	//把文件光标的位置定位到离开头0x12个字节处
	lseek(fd_pic,0x12,SEEK_SET);
	char buf1[4]; 
	read(fd_pic,buf1,4);
	//获取图片宽度
	int width = (buf1[0] & 0xff) |
			((buf1[1] & 0xff) << 8) | 
			((buf1[2] & 0xff) << 16) | 
			((buf1[3] & 0xff) << 24);
	
	lseek(fd_pic,0x16,SEEK_SET);
	read(fd_pic,buf1,4);
	//获取图片高度
	int height = (buf1[0] & 0xff) |
			((buf1[1] & 0xff) << 8) | 
			((buf1[2] & 0xff) << 16) | 
			((buf1[3] & 0xff) << 24); 
	
	//显示图片宽度高度
	//printf("size = %d * %d \n",width,height);
	
	//判断图片是否过大
	if(((width + x)> 800)||((y + height)> 480))
	{
		printf("pic input error\n");
		return;
	}
	int color;
	int w,h,i = 0;
	unsigned char r,g,b;
	for(h = 0;h < height;h++)
	{
		for(w = 0;w < width;w++)
		{
			b = buf[54 + i++];
			g = buf[54 + i++];
			r = buf[54 + i++];
			color = ((r << 16)|(g << 8)|(b));
			//int dy = hight > 0?((y0+hight-1)-y):(y0+y);
			//lcd_draw_point(x+x0,dy,color);
			draw_point((w + x),((y + height - 1)- h),color);
		}
	}
	close(fd_pic);
}
